DROP TABLE APP.ANIMAL;
DROP TABLE APP.FEED;
DROP TABLE APP.STAFF;

CREATE TABLE APP.ANIMAL(
ANIMAL_ID INT NOT NULL,
ANIMAL_CHIP VARCHAR(50),
ANIMAL_BIRTH VARCHAR(10),
ANIMAL_SPECIE VARCHAR(50),
ANIMAL_NAME VARCHAR(50),
constraint PK_HISTORY primary key (ANIMAL_ID)
);

CREATE TABLE APP.STAFF(
STAFF_ID INT NOT NULL,
STAFF_PERSONAL_ID VARCHAR(50),
STAFF_NAME VARCHAR(50),
STAFF_LASTNAME VARCHAR(50),
STAFF_BIRTH VARCHAR(10),
STAFF_CONTACT VARCHAR(50),
constraint PK_STAFF primary key (STAFF_ID)
);

CREATE TABLE APP.FEED(
FEED_ID INT NOT NULL,
FEED_NAME VARCHAR(50),
FEED_AMOUNT INT,
FEED_MINIMUM INT,
constraint PK_FEED primary key (FEED_ID)
);